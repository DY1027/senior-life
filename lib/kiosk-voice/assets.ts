const availableVoiceIds = new Set(AVAILABLE_KIOSK_VOICE_IDS);

/** 같은 문장은 언제나 같은 파일명을 쓰도록 만드는 짧고 안정적인 해시. */
export function kioskVoiceId(text: string): string {
  let hash = 0x811c9dc5;
  for (let index = 0; index < text.length; index += 1) {
    hash ^= text.charCodeAt(index);
    hash = Math.imul(hash, 0x01000193);
  }
  return `guide-${(hash >>> 0).toString(16).padStart(8, "0")}`;
}

export function kioskVoiceAsset(text: string): string | undefined {
  const id = kioskVoiceId(text);
  return availableVoiceIds.has(id) ? `/api/kiosk-voice/${id}` : undefined;
}
