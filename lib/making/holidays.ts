// 달력에 빨간 날로 표시할 공휴일. 음력 명절(설·추석·부처님오신날)은 해마다
// 날짜가 바뀌므로 연 1회 갱신 필요. 대체공휴일은 규칙이 복잡해 표시하지 않는다
// (공식 날짜만 빨갛게 — 틀린 빨간 날을 만드는 것보다 안전).
// key: "M-D" (월-일), value: 달력에 작게 적을 이름
export const HOLIDAYS: Record<number, Record<string, string>> = {
  2026: {
    "1-1": "신정",
    "2-16": "설 연휴",
    "2-17": "설날",
    "2-18": "설 연휴",
    "3-1": "삼일절",
    "5-5": "어린이날",
    "5-24": "부처님오신날",
    "6-6": "현충일",
    "8-15": "광복절",
    "9-24": "추석 연휴",
    "9-25": "추석",
    "9-26": "추석 연휴",
    "10-3": "개천절",
    "10-9": "한글날",
    "12-25": "성탄절",
  },
};

export function getHoliday(year: number, month: number, day: number): string | null {
  return HOLIDAYS[year]?.[`${month}-${day}`] ?? null;
}
